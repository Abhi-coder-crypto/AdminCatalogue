const API_URL = "http://localhost:5000/api/celebrities";

async function addActors() {
  try {
    console.log("Logging in as admin...");
    
    const loginResponse = await fetch("http://localhost:5000/api/auth/login", {
      method: "POST",
      headers: { "Content-Type": "application/json" },
      credentials: "include",
      body: JSON.stringify({
        email: "Abhi@gmail.com",
        password: "Abhi@123",
      }),
    });

    if (!loginResponse.ok) {
      const error = await loginResponse.text();
      throw new Error(`Login failed: ${loginResponse.statusText} - ${error}`);
    }

    const setCookieHeader = loginResponse.headers.get('set-cookie');
    
    if (!setCookieHeader) {
      console.log("Response headers:", Object.fromEntries(loginResponse.headers.entries()));
      throw new Error("No session cookie received");
    }

    const sessionCookie = setCookieHeader.split(';')[0];

    console.log("\nAdding 20 actors to the database...\n");

    for (const actor of actors) {
      try {
        const response = await fetch(API_URL, {
          method: "POST",
          headers: {
            "Content-Type": "application/json",
            "Cookie": sessionCookie,
          },
          body: JSON.stringify(actor),
        });

        if (response.ok) {
          const result = await response.json();
          console.log(`✓ Added: ${actor.name} (ID: ${result._id})`);
        } else {
          const error = await response.json();
          console.error(`✗ Failed to add ${actor.name}: ${error.message}`);
        }
      } catch (error) {
        console.error(`✗ Error adding ${actor.name}:`, error);
      }
    }

    console.log("\n✓ All actors have been processed!");
  } catch (error) {
    console.error("Error:", error);
    process.exit(1);
  }
}

addActors();
